def sensitivity_sweep(deltas=(("quality", 0.15), ("earn_quality", 0.15),
                              ("dominance", 0.15), ("capex_elastic", 0.15))):
    """Re-derive the recovery ranking with each judgment score shifted +/- delta,
    to reveal which orderings are robust and which depend on a chosen number.
    This is the honesty check: it runs only the (fast) decomposition, not the
    full Monte Carlo, because the recovery RANKING is set by recovery_quality.
    """
    print("=" * 74)
    print("TABLE 5 -- Sensitivity: recovery-rank stability under +/-score shifts")
    print("           robust tiers vs. calibration-dependent fine ordering")
    print("=" * 74)
    scenarios = {"base": []}
    for field, d in deltas:
        scenarios[f"{field}+"] = [(field, +d)]
        scenarios[f"{field}-"] = [(field, -d)]

    rankings = {}
    for label, shifts in scenarios.items():
        comp = copy.deepcopy(C.COMPANIES)
        for field, d in shifts:
            for v in comp.values():
                v[field] = float(np.clip(v[field] + d, 0, 1))
        dec = E.decompose_premium(comp)
        rankings[label] = sorted(dec, key=lambda n: -dec[n]["recovery_quality"])

    names = list(C.COMPANIES.keys())
    cols = list(scenarios.keys())
    # print as rank rows
    print(f"{'Rank':5s} " + " ".join(f"{c:13s}" for c in cols[:5]))
    for i in range(len(names)):
        print(f"{i+1:4d}  " + " ".join(f"{rankings[c][i]:13s}" for c in cols[:5]))
    print("  (showing first 5 scenario columns; full set saved to results.json)")
    print()

    # robustness summary: who never moves from top/bottom tier
    top_always = set(rankings["base"][:3])
    bot_always = set(rankings["base"][-2:])
    for label in rankings:
        top_always &= set(rankings[label][:3])
        bot_always &= set(rankings[label][-2:])
    print(f"  ROBUST top tier (recovers well in every scenario): {sorted(top_always)}")
    print(f"  ROBUST bottom tier (recovers poorly in every scenario): {sorted(bot_always)}")
    print()
    return rankings


def reconciliation_note():
    print("=" * 74)
    print("SYSTEMIC-TAIL RECONCILIATION (two independent methods)")
    print("=" * 74)
    d = C.derive_regime_probs()
    print("  Simulation tail (emergent) vs. hazard-derived tail vs. reconciled range:")
    for i, hz in enumerate(C.HORIZON_LABELS):
        sim = C.REGIME_PROBS["systemic"][i]
        haz = d["systemic"][i]
        lo, hi = C.SYSTEMIC_TAIL_RECONCILED[hz]
        print(f"    {hz:4s}  calibrated={sim*100:4.1f}%   hazard={haz*100:4.1f}%   "
              f"reconciled={lo*100:.0f}-{hi*100:.0f}%")
    print()


def main():
    print("#" * 74)
    print(f"#  SYSTEMIC + TAM RISK MODEL   |   calibrated as of {C.AS_OF_DATE}")
    print(f"#  {C.SIM['n_paths']:,} paths  |  horizons {C.HORIZON_LABELS}  |  mandate {int(C.MANDATE_MONTHS)}m (suggested edge)")
    _ss = C.SYSTEMIC_STRESS
    print(f"#  macro-stress index {_ss['score']*100:.0f}/100 "
          f"(structural {_ss['structural']*100:.0f} / acute {_ss['acute']*100:.0f}) "
          f"-> crisis x{C.STRESS_CRISIS_MULT:.2f}")
    print(f"#  THIS IS A RESEARCH MODEL, NOT INVESTMENT ADVICE.")
    print("#" * 74)

    print("\n[1/3] Running broad-exposure Monte Carlo ...")
    eres, thr = E.run_exposures(verbose=True)
    print_exposure_tables(eres, thr)
    print_recovery_table(eres)

    print("[2/3] Running TAM-adjusted company Monte Carlo ...")
    cres, cthr = E.run_companies(verbose=True)
    print_company_tables(cres, cthr)

    print("[3/3] Sensitivity sweep + reconciliation ...")
    rankings = sensitivity_sweep()
    reconciliation_note()

    # ---- save everything to one results file (numpy -> python types) ----
    def clean(o):
        if isinstance(o, dict):  return {k: clean(v) for k, v in o.items()}
        if isinstance(o, (list, tuple)): return [clean(x) for x in o]
        if isinstance(o, (np.floating,)): return float(o)
        if isinstance(o, (np.integer,)):  return int(o)
        return o
    results = {
        "as_of": C.AS_OF_DATE,
        "macro": C.MACRO,
        "regime_probs": C.REGIME_PROBS,
        "exposures": clean(eres),
        "companies": clean(cres),
        "sensitivity": rankings,
        "thresholds": list(thr),
    }
    with open("results.json", "w") as f:
        json.dump(_clean_json(results), f, indent=1, allow_nan=False)
    print("Saved results.json  (feed this to the report generator).")
    print("\nDONE.  To update: edit calibration.py, re-run this file.")


if __name__ == "__main__":
    main()
