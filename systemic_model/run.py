def _clean_json(x):
    """Replace NaN/Inf with None so generated JSON is strict-parser safe."""
    import math as _m
    if isinstance(x, dict):
        return {k: _clean_json(v) for k, v in x.items()}
    if isinstance(x, (list, tuple)):
        return [_clean_json(v) for v in x]
    if isinstance(x, float) and (_m.isnan(x) or _m.isinf(x)):
        return None
    return x


def _fmt_pct(x, width=4):
    return ("{:>%d.0f}" % width).format(x * 100) if x == x else f"{'n/a':>{width}}"


def print_exposure_tables(results, thr):
    print("\n" + "=" * 74)
    print("TABLE 1 -- Broad exposures: P(max drawdown > X within horizon)")
    print("=" * 74)
    header = f"{'Exposure':18s} {'Hz':4s} " + " ".join(f">{int(t*100):>2d}%" for t in thr)
    print(header)
    for nm, byhz in results.items():
        for hz in C.HORIZON_LABELS:
            row = byhz[hz]["exceedance"]
            print(f"{nm:18s} {hz:4s} " + " ".join(_fmt_pct(v) for v in row))
        print()


